# Defining a string with escape sequences
tabby_cat = "\tI'm tabbed in."
persian_cat = "I'm split\non a line."
backslash_cat = "I'm a \\ a cat."

# Multi-line string with triple quotes
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

# Printing the strings
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
